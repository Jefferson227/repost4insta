    public void showEdit() {
        try {
            if (instagramAccountEdit != null) {
                if (!instagramAccountEdit.isConnect()) {
                    instagramAccountEdit.connect();
                }
                 showImage(panelImage, ImageFromUrl.get(instagramAccountEdit.getUrlProfilePic()));
                 txtUserName.setText(instagramAccountEdit.getUsername());
                 txtPass.setText(instagramAccountEdit.getPassword());
                 btnAdd.setEnabled(true);
                 showTable(accountTargetDao.listByAccount(instagramAccountEdit.getUsername()));
            }
        } catch (Exception e) {
            showErrorMessage(e);
        }
    }

