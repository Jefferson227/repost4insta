    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (verifyEmpytFields("All fields are necessary", txtUserName, txtPass)) {
            try {
                InstagramAccount instagramAccount = new InstagramAccount(txtUserName.getText(),
                        String.valueOf(txtPass.getPassword()));
                showImage(panelImage, ImageFromUrl.get(instagramAccount.getUrlProfilePic()));
                InstagramAccountDao instagramAccountDao = new InstagramAccountDao();
                instagramAccountDao.doSave(instagramAccount);
                showInformationMessage("Connection success");
                btnAdd.setEnabled(true);
                instagramAccountEdit = instagramAccount;
            } catch (Exception ex) {
                ex.printStackTrace();
                showErrorMessage(ex);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

